-- define generating function
-- return type: 3D Tensor
-- 
function singleGenerate(dataset,model)
   -- local vars
   local time = sys.clock()

   -- test over given dataset
   print('<detecter> on target image:')
   
   -- disp progress
   xlua.progress(1, 1)

   -- create mini batch
   local input = dataset:clone()

   -- test samples
   local output = model:forward(input):clone()

   -- timing
   time = sys.clock() - time
   print('<detecter> time to generate 1 sample = '..(time*1000)..'ms')
   
   return output
end
